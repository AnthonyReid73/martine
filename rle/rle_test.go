package rle

import (
	"testing"
)

func TestRleEncode(t *testing.T) {
	in := []byte{0x10, 0x10, 0x10,
		0x20, 0x20,
		0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30,
		0x40,
		0x50}
	out := Encode(in)
	if len(out) != 10 {
		t.Fatalf("Expected 10 elements and gets %d\n", len(out))
	}
	t.Log(out)

	in = []byte{0x10, 0x10, 0x10,
		0x20, 0x20,
		0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30,
		0x40,
		0x50,
		0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30, 0x30}
	out = Encode(in)
	t.Log(out)
}
