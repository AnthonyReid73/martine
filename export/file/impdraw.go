var splitRasterPattern = []byte{0xed, 0x69, 0x0e, 0x00}

func ExportSplitRaster(filename string, p color.Palette, rasters []*constants.SplitRaster, exportType *export.ExportType) error {

	var binarySplitRaster []byte
	binarySplitRaster = splitRasterTemplate
	for _, v := range rasters {
		offset := OffsetAtIndex(binarySplitRaster, splitRasterTemplate, v.Occurence)
		if offset == -1 {
			fmt.Fprintf(os.Stderr, "Occurence [%d] not found in split raster template\n", v.Occurence)
			continue
		}
		binarySplitRaster[offset] = byte(v.PaletteIndex)
	}

	header := cpc.CpcHead{Type: 0, User: 0, Address: 0x170, Exec: 0x0,
		Size:        uint16(binary.Size(binarySplitRaster)),
		Size2:       uint16(binary.Size(binarySplitRaster)),
		LogicalSize: uint16(binary.Size(binarySplitRaster))}
	copy(header.Filename[:], exportType.GetAmsdosFilename(filename, ".SPL"))
	header.Checksum = uint16(header.ComputedChecksum16())
	basicPath := exportType.OutputPath + string(filepath.Separator) + exportType.GetAmsdosFilename(filename, ".SPL")
	fw, err := os.Create(basicPath)
	if err != nil {
		fmt.Fprintf(os.Stderr, "Error while creating file (%s) error :%s\n", basicPath, err)
		return err
	}
	if !exportType.NoAmsdosHeader {
		binary.Write(fw, binary.LittleEndian, header)
	}
	binary.Write(fw, binary.LittleEndian, binarySplitRaster)
	fw.Close()

	exportType.AddFile(basicPath)
	return nil
}

func OffsetAtIndex(b []byte, pattern []byte, occurence int) int {
	i := 0
	founds := 0
	for {
		v := b[i : i+len(pattern)]
		if bytes.Compare(v, pattern) == 0 && founds == occurence {
			return i + len(pattern)
		}
		i++
		if i >= len(b) {
			return -1
		}
	}
}
